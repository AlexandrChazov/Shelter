pet_3.addEventListener('click', () => {
    popup__window.children[0].src = fullPetsList[3].img;
    popup__window.children[1].children[0].innerText = fullPetsList[3].name;
    popup__window.children[1].children[1].innerText = `${fullPetsList[3].type} - ${fullPetsList[3].breed}`;
    popup__window.children[1].children[2].innerText = fullPetsList[3].description;
    popup_span_1.nextSibling.innerText = fullPetsList[3].age;
    popup_span_2.nextSibling.innerText = fullPetsList[3].inoculations;
    popup_span_3.nextSibling.innerText = fullPetsList[3].diseases;
    popup_span_4.nextSibling.innerText = fullPetsList[3].parasites;

    overlay.classList.add('dark');
    popup.classList.remove('popup__hide');
});

